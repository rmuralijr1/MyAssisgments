package week1.day2;

public class FindOddNumbers {

	public static void main(String[] args) {
					
			for (int A = 1; A <=10; A++) {
				
				if(A%2==1)
				
				System.out.print(A);
			
			
		}
			
	
	
	for (int i = 10; i >=1; i--) {
		
		if(i%2==1)
		
		System.out.println(i);

}
	}}


			


	
